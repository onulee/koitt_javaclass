
public class MyMath {
	// add메소드
	long add(long a,long b) {
		long result= a + b;
		return result;
	}
	
	// sub메소드
	long sub(long a,long b) {
		long result = a-b;
		return result;
	}
	
	//multi메소드
	long multi(long a, long b) {
		long result =a*b;
		return result;
	}
	
	//div메소드
	long div(long a,long b) {
		long result = a/b;
		return result;
	}

}
